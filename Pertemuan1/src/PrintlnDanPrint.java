public class PrintlnDanPrint {
  public static void main(String[] args) {
    // println -> dia memiliki value enter di akhir
    System.out.println("ini adalah contoh <-- Println --> : ");
    System.out.println("hello");
    System.out.println("world");

    // print -> dia seperti biasa tidak memiliki value enter di akhir
    System.out.println("ini adalah contoh <-- Print --> : ");
    System.out.print("hello");
    System.out.print("world");
  }
}
