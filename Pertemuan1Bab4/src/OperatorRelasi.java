public class OperatorRelasi {
  public static void main(String[] args) {
    // a few numbers
    int i = 37;
    int j = 42;
    int k = 42;
    // lebih besar dari
    System.out.println(" k > j = " + (k > j)); // false
    // lebih besar atau sama dengan
    System.out.println(" k >= j = " + (k >= j)); // true
    // lebih kecil dari
    System.out.println(" k < j = " + (k < j)); // false
    // lebih kecil atau sama dengan
    System.out.println(" k <= j = " + (k <= j)); // true
    // sama dengan
    System.out.println(" k == j = " + (k == j)); // true
    // tidak sama dengan
    System.out.println(" k != j = " + (k != j)); // false
  }
}
